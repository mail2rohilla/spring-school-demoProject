package com.deepanshu.pojos;

public class Demo1 {
    int ba;
    int ca;
    int da;

    public Demo1() {
    }

    public Demo1(int ba, int ca, int da) {
        this.ba = ba;
        this.ca = ca;
        this.da = da;
    }

    public int getBa() {
        return ba;
    }

    public int getCa() {
        return ca;
    }

    public int getDa() {
        return da;
    }

    public void setBa(int ba) {
        this.ba = ba;
    }

    public void setCa(int ca) {
        this.ca = ca;
    }

    public void setDa(int da) {
        this.da = da;
    }
}


